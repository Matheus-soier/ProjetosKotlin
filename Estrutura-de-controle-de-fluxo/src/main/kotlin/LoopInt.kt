fun main() {
    for(i in 0..3) { //Loop de 0 a 3
        print(i)
    }
    print(" ")
    for (i in 0 until 3) { //Loop de 0 a 2
        print(i)
    }
    print(" ")
    for(i in 2..10 step 2) { //Loop de 2 até 10 de 2 em 2
        print(i)
    }
    print(" ")
    for (i in 3 downTo 0) { //Loop decrementando de 3 até 0
        print(i)
    }


}